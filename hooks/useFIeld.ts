const SIZE = 10
export const useField = () => {

    const [playerPos, setPlayerPos] = useState({ x: Math.round(SIZE / 2), y: Math.round(SIZE / 2) })
    const field: Array<Array<number>> = Array.from({ length: SIZE }, (_, i) => Array.from({ length: SIZE }, (_, j) => (i + j) % 2));
    const { isActive } = useMenu()

    const handleKeydown = useCallback((e: KeyboardEvent) => {
        if (isActive) return
        switch (e.key) {
            case "ArrowUp":
            case "w":
                moveUp()
                break;
            case "ArrowDown":
            case "s":
                moveDown()
                break;
            case "ArrowLeft":
            case "a":
                moveLeft()
                break;
            case "ArrowRight":
            case "d":
                moveRight()
                break;
        }
    }, [isActive])
    const moveUp = () => {
        move(0, -1)

    }
    const moveDown = () => {
        move(0, 1)
    }
    const moveLeft = () => {
        move(-1, 0)
    }
    const moveRight = () => {
        move(1, 0)
    }
    const isWithinField = (x: number, y: number) => {
        if (x >= 0 && y >= 0 &&
            x < field.length && y < field.length) return true
        return false
    }
    const move = (x: number, y: number) => {
        setPlayerPos((prev) => {
            if (!isWithinField(prev.x + x, prev.y + y)) return prev
            return ({ ...prev, x: prev.x + x, y: prev.y + y })
        })
    }
    const handleWheel = (e: WheelEvent) => {
        let scale = Number(document.documentElement.style.getPropertyValue("--scale"))
        if (e.deltaY > 0) {
            scale -= 0.1
        }
        else {
            scale += 0.1
        }
        if (scale >= 0.3 && scale <= 4) {
            document.documentElement.style.setProperty("--scale", `${scale}`)
        }
    }
    useEffect(() => {
        window.addEventListener("keydown", handleKeydown)
        window.addEventListener("wheel", handleWheel)
        return () => {
            window.removeEventListener("keydown", handleKeydown)
            window.removeEventListener("wheel", handleWheel)
        }
    }, [handleKeydown])

    useEffect(() => {
        document.documentElement.style.setProperty('--size', `${field.length}`);
        document.documentElement.style.setProperty('--scale', `${1}`);
    }, [])
    
    return { field, playerPos };
};